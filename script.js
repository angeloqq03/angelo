function toggleNav() {
  const navLinks = document.getElementById("navLinks");
  navLinks.classList.toggle("active");
}

function showModal() {
  const modal = document.getElementById("myModal");
  modal.style.display = "block";
}

function closeModal() {
  const modal = document.getElementById("myModal");
  modal.style.display = "none";
}

function seeTotal() {
  const checkboxes = document.querySelectorAll('.service-checkbox:checked');
  let totalAmount = 0;
  checkboxes.forEach(function (checkbox) {
    const price = parseInt(checkbox.getAttribute('data-price'));
    const term = parseInt(checkbox.parentElement.querySelector('.term-input').value);
    totalAmount += price * term;
  });

  const totalAmountElement = document.getElementById("total");
  totalAmountElement.textContent = totalAmount;
  showModal();
}


function submitForm() {
  const totalAmountElement = document.getElementById("total");
  const selectedTerms = parseInt(document.getElementById("terms").value);
  const totalAmount = parseInt(totalAmountElement.textContent);
  const totalPrice = totalAmount * selectedTerms;

  alert(`Total Amount: PHP${totalPrice}`);
  closeModal();
}
